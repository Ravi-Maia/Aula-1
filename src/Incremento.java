
public class Incremento {
	public static void main(String[] args) {
		
		// Pegadinha de prova!!!
		
		int num = 5;
		
		int x = num++;
		
		System.out.println(x); 
		System.out.println(num);
		
		int j = 10;
		int z = ++j;

		System.out.println(j); 
		System.out.println(z);
		
		int k = 100;
		int l = k--;
		
		System.out.println(l); 
		
		int a = 100;
		int b = --a;
		
		System.out.println(b); 
	}
}
